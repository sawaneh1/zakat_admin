export type TranslationKey = keyof typeof translations.en

export function t(key: TranslationKey, locale: Locale = defaultLocale): string {
  const strings = translations[locale] || translations.en
  return (strings as Record<string, string>)[key] || (translations.en as Record<string, string>)[key] || key
}

export function localized(
  field: { en: string; ar?: string } | string | null | undefined,
  locale: Locale = defaultLocale
): string {
  if (!field) return ''
  if (typeof field === 'string') return field
  return (locale === 'ar' && field.ar) ? field.ar : field.en
}

export function isRTL(locale: Locale): boolean {
  return locale === 'ar'
}

export function getDir(locale: Locale): 'rtl' | 'ltr' {
  return locale === 'ar' ? 'rtl' : 'ltr'
}
